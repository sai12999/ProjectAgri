package com.agriproject.entity;

/**
 * @author DELL
 *
 */
public class Farmer {
	private String nameOfFarmer;
	private String mobileNumberOfFarmer;
	private String acres;
	private String location;
	private String pincode;

	
	/**
	 * @return the nameOfFarmer
	 */
	public String getNameOfFarmer() {
		return nameOfFarmer;
	}

	/**
	 * @param nameOfFarmer the nameOfFarmer to set
	 */
	public void setNameOfFarmer(String nameOfFarmer) {
		this.nameOfFarmer = nameOfFarmer;
	}

	/**
	 * @return the mobileNumberOfFarmer
	 */
	public String getMobileNumberOfFarmer() {
		return mobileNumberOfFarmer;
	}

	/**
	 * @param mobileNumberOfFarmer the mobileNumberOfFarmer to set
	 */
	public void setMobileNumberOfFarmer(String mobileNumberOfFarmer) {
		this.mobileNumberOfFarmer = mobileNumberOfFarmer;
	}

	/**
	 * @return the acres
	 */
	public String getAcres() {
		return acres;
	}

	/**
	 * @param acres the acres to set
	 */
	public void setAcres(String acres) {
		this.acres = acres;
	}

	/**
	 * @return the location
	 */
	public String getLocation() {
		return location;
	}

	/**
	 * @param location the location to set
	 */
	public void setLocation(String location) {
		this.location = location;
	}

	/**
	 * @param pincode the pincode to set
	 */
	public void setPincode(String pincode) {
		this.pincode = pincode;
	}
	
	/**
	 * @return the pincode
	 */
	public String getPincode() {
		return pincode;
	}

}
